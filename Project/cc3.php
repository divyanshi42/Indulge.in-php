					<p>Before emerging the cloud computing, there was Client/Server computing which is
						 basically a centralized storage in which all the software applications, all the
						  data and all the controls are resided on the server side.</p>

					<p>If a single user wants to access specific data or run a program, he/she need to
						 connect to the server and then gain appropriate access, and then he/she can do
						  his/her business.</p>
					<p>Then after, distributed computing came into picture, where all the computers
						 are networked together and share their resources when needed.</p>
					<p>On the basis of above computing, there was emerged of cloud computing concepts
						 that later implemented.</p>
					<p>At around in 1961, John MacCharty suggested in a speech at MIT that computing
						 can be sold like a utility, just like a water or electricity. It was a
						 brilliant idea, but like all brilliant ideas, it was ahead if its time,
						  as for the next few decades, despite interest in the model, the technology
							 simply was not ready for it.</p>
					<p>But of course time has passed and the technology caught that idea and
						after few years we mentioned that:</p>
			  	<p>In 1999, Salesforce.com started delivering of applications to users using a
						simple website. The applications were delivered to enterprises over the
						Internet, and this way the dream of computing sold as utility were true.</p>
		   		<p>In 2002, Amazon started Amazon Web Services, providing services like storage,
						 computation and even human intelligence. However, only starting with the
						  launch of the Elastic Compute Cloud in 2006 a truly commercial service
							open to everybody existed.</p>
					<p>In 2009, Google Apps also started to provide cloud computing enterprise applications.</p>

					<hr>
					<p>Of course, all the big players are present in the cloud computing evolution,
						some were earlier, some were later. In 2009, Microsoft launched Windows Azure,
						 and companies like Oracle and HP have all joined the game. This proves that
						  today, cloud computing has become mainstream.</p>



