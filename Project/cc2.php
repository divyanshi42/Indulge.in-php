					<h2 class="blog-post-title">Advantages of Cloud Computing</h2>
					<p class="blog-post-meta">January 1, 2021 by <a href="#">Nirbhay Narayan Singh</a></p>

					<p>As we all know that Cloud computing is trending technology. Almost every company switched their services on the cloud to rise the company growth.</p>
					<hr>
					<p>Here, we are going to discuss some important advantages of Cloud Computing-</p>
					<img src="https://static.javatpoint.com/cloudpages/images/advantages-of-cloud-computing.png" height="200px" width="300px">
					<br>
					<br>
					<ol type="1">
						<li><blockquote>Back-up and restore data</blockquote></li>
						<p>Once the data is stored in the cloud, it is easier to get back-up and restore that data using the cloud.</p>
						<li><blockquote>Improved collaboration</blockquote></li>
						<p>Cloud applications improve collaboration by allowing groups of people to quickly and easily share information in the cloud via shared storage.</p>
						<li><blockquote>Excellent accessibility</blockquote></li>
						<p>Cloud allows us to quickly and easily access store information anywhere, anytime in the whole world,
							 using an internet connection. An internet cloud infrastructure increases organization productivity
							  and efficiency by ensuring that our data is always accessible.</p>
						<li><blockquote>Low maintenance cost</blockquote></li>
						<p>Cloud computing reduces both hardware and software maintenance costs for organizations.</p>
						<li><blockquote>Mobility</blockquote></li>
						<p>Cloud computing allows us to easily access all cloud data via mobile.</p>
						<li><blockquote>IServices in the pay-per-use model</blockquote></li>
						<p>Cloud computing offers Application Programming Interfaces (APIs) to the users for access services on the
							 cloud and pays the charges as per the usage of service.</p>
						<li><blockquote>Unlimited storage capacity</blockquote></li>
						<p>Cloud offers us a huge amount of storing capacity for storing our important data such as documents,
							 images, audio, video, etc. in one place.</p>
						<li><blockquote>Data security</blockquote></li>
						<p>Data security is one of the biggest advantages of cloud computing. Cloud offers many advanced features
							related to security and ensures that data is securely stored and handled.</p>
					</ol>
					<br>
					<br>
					<h2>Disadvantages of Cloud Computing</h2>
					<p>A list of the disadvantage of cloud computing is given below -</p>
					<ol type="1">
						<li><blockquote>Internet Connectivity</blockquote></li>
						<p>As you know, in cloud computing, every data (image, audio, video, etc.) is stored on the cloud,
							 and we access these data through the cloud by using the internet connection. If you do not
							 have good internet connectivity, you cannot access these data. However, we have no any
							 other way to access data from the cloud.</p>
						<li><blockquote>Vendor lock-in</blockquote></li>
						<p>Vendor lock-in is the biggest disadvantage of cloud computing. Organizations may face
							problems when transferring their services from one vendor to another. As different
							vendors provide different platforms, that can cause difficulty moving from one cloud to another.</p>
						<li><blockquote>Limited Control</blockquote></li>
						<p>As we know, cloud infrastructure is completely owned, managed, and monitored by the service
							provider, so the cloud users have less control over the function and execution of services
							within a cloud infrastructure.</p>
						<li><blockquote>Security</blockquote></li>
						<p>Although cloud service providers implement the best security standards to store important
							information. But, before adopting cloud technology, you should be aware that you will be
							sending all your organization's sensitive information to a third party, i.e., a cloud
							computing service provider. While sending the data on the cloud, there may be a chance
							 that your organization's information is hacked by Hackers.</p>

					</ol>


