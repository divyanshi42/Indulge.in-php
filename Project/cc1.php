            <h2 class="blog-post-title">Starting with cloud computing</h2>
            <p class="blog-post-meta">January 1, 2021 by <a href="#">Nirbhay Narayan Singh</a></p>

            <p>Cloud Computing tutorial provides basic and advanced concepts of Cloud Computing. Our Cloud Computing tutorial is designed for beginners and professionals.</p>
            <hr>
            <p>Cloud computing is a virtualization-based technology that allows us to create, configure,
							and customize applications via an internet connection. The cloud technology includes a
							development platform, hard disk, software application, and database.</p>

            <h2>What is Cloud Computing</h2>
            <p>The term cloud refers to a network or the internet. It is a technology that uses remote servers on the internet to
							 store, manage, and access data online rather than local drives. The data can be anything such as files, images,
							  documents, audio, video, and more.</p>
						<p>There are the following operations that we can do using cloud computing:</p>
						<ul>
							<li>Developing new applications and services</li>
							<li>Storage, back up, and recovery of data</li>
							<li>Hosting blogs and websites</li>
							<li>Delivery of software on demand</li>
							<li>Analysis of data</li>
							<li>Streaming videos and audios</li>
						</ul>
            <h3>Why Cloud Computing?</h3>
            <p>Small as well as large IT companies, follow the traditional methods to provide the IT infrastructure. That means for
							 any IT company, we need a Server Room that is the basic need of IT companies.</p>

            <p>In that server room, there should be a database server, mail server, networking, firewalls, routers, modem,
							 switches, QPS (Query Per Second means how much queries or load will be handled by the server),
							 configurable system, high net speed, and the maintenance engineers.</p>
						<p>To establish such IT infrastructure, we need to spend lots of money. To overcome
							all these problems and to reduce the IT infrastructure cost, Cloud Computing comes into existence.</p>
            <h3>Characteristics of Cloud Computing</h3>
            <p>The characteristics of cloud computing are given below:</p>
            <ol type="1">
              <li><blockquote>Agility</blockquote></li>
							<p>The cloud works in a distributed computing environment. It shares resources among users and works very fast.</p>
							<li><blockquote>High availability and reliability</blockquote></li>
							<p>The availability of servers is high and more reliable because the chances of infrastructure failure are minimum.</p>
							<li><blockquote>High Scalability</blockquote></li>
							<p>Cloud offers "on-demand" provisioning of resources on a large scale, without having engineers for peak loads.</p>
							<li><blockquote>Multi-Sharing</blockquote></li>
							<p>With the help of cloud computing, multiple users and applications can work more efficiently with cost reductions by sharing common infrastructure.</p>
							<li><blockquote>Device and Location Independence</blockquote></li>
							<p>Cloud computing enables the users to access systems using a web browser regardless of their location or what device they use e.g. PC, mobile
								 phone, etc. As infrastructure is off-site (typically provided by a third-party) and accessed via the Internet, users can connect from anywhere.</p>
							<li><blockquote>Maintenance</blockquote></li>
							<p>Maintenance of cloud computing applications is easier, since they do not need to be installed on each
								 user's computer and can be accessed from different places. So, it reduces the cost also.</p>
							<li><blockquote>Low Cost</blockquote></li>
	 						<p>By using cloud computing, the cost will be reduced because to take the services of
								cloud computing, IT company need not to set its own infrastructure and pay-as-per usage of resources.</p>
							<li><blockquote>Services in the pay-per-use mode</blockquote></li>
							<p>Application Programming Interfaces (APIs) are provided to the users so that they can access services on
								 the cloud by using these APIs and pay the charges as per the usage of services.</p>
            </ol>

